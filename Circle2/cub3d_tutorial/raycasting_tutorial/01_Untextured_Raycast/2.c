{
	double pos_x;
	double pos_y;
	double dir_x;
	double dir_y;
	double plane_x;
	double plane_y;
	double move_speed;
	double rotation_speed;
} t_player;

void	player_init(t_player *player)
{
	// x and y are player position
	player->pos_x = 12;
	player->pos_y = 5;

	// initial direcion vector
	player->dir_x = -1;
	player->dir_y = 0;

	// camera plane of player
	player->plane_x = 0;
	player->plane_y = 0.66;

	player->move_speed = 0.05;
	player->rotation_speed = 0.05;
}

